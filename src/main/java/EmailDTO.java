/**
 * The EmailDTO class represents a Data Transfer Object (DTO) for holding email information.
 */
public class EmailDTO {

    private String email;

    /**
     * Gets the email content.
     *
     * @return the email content as a string.
     */
    public String getEmail() {
        return email;
    }

    /**
     * Sets the email content.
     *
     * @param email the email content to set.
     */
    public void setEmail(String email) {
        this.email = email;
    }
}
